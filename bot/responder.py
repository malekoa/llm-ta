def remove_previous_footer(body: str) -> str:
    """Remove any previously added disclaimer blocks from bot replies."""
    pattern = re.compile(r'<!-- footer-start -->.*?<!-- footer-end -->', re.DOTALL)
    return pattern.sub('', body).strip()
